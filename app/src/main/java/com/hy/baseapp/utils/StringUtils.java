    /**
     * 保留小数点后2位数字
     *
     * @param num
     * @return
     */
    public static BigDecimal get2Num(String num) {
        if (TextUtils.isEmpty(num)) num = "0";
        BigDecimal bd = new BigDecimal(num);
        bd = bd.setScale(2, BigDecimal.ROUND_HALF_UP);
        return bd;
    }

